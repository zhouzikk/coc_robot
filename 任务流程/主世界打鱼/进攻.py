        if not 是否匹配:
            #上下文.置脚本状态(f"当前没有可下的{兵种}")
            return False

        偏移x, 偏移y = 偏移
        兵种位置 = (检测区域[0] + 偏移x, 检测区域[1] + 偏移y)

        # 状态检测流程优化
        是否有白框, 白框列表, _ = self.检测白色矩形框(屏幕图像)
        if 是否有白框 and self.判断点是否被白框包裹((偏移x, 偏移y), 白框列表):
            return True  # 已选中状态

