        图块区域 = 上下文.op.获取屏幕图像cv(
            兵种位置[0], 兵种位置[1],
            兵种位置[0] + 20, 兵种位置[1] + 20
        )
        if self.是否为灰色图片(图块区域):
            上下文.置脚本状态(f"{兵种}已全部下完")
            return False

